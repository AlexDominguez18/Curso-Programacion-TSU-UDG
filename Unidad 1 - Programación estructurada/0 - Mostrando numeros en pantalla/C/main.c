#include <stdio.h>

int main ()
{
    /**
     * En el siguiente ejemplo podemos observar que se hace uso de "%d"
     * para indicarle al programa que vamos a mostrar un digito numerico.
     * Posteriormente, le pasamos el numero 23 para que lo muestre en pantalla.
    */
    printf("Mi nombre es Alejandro y mi edad es: %d\n", 23);

    return 0;
}