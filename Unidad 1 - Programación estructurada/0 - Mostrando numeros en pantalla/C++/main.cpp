#include <iostream>

using namespace std;

int main()
{
    /**
     * En el siguiente ejemplo notamos que a diferencia de C, aqui no necesitamos
     * hacer uso de la funcion 'printf' por lo que solo vamos a mandar lo que queremos
     * mostrar en pantalla haciendo uso de los operadores '<<' para la salida de datos.
    */
    cout << "Mi nombre es Alejandro y mi edad es " << 23 << endl;

    return 0;
}